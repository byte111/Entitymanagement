package com.entitymanagement.vo;

public class ProductAttributeVO {

	
	private String productId;
	private String attriuteId;
	private String attributeValueId;
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getAttriuteId() {
		return attriuteId;
	}
	public void setAttriuteId(String attriuteId) {
		this.attriuteId = attriuteId;
	}
	public String getAttributeValueId() {
		return attributeValueId;
	}
	public void setAttributeValueId(String attributeValueId) {
		this.attributeValueId = attributeValueId;
	}
	
	
}
