package com.entitymanagement.vo;

public class AttributeValue {
	private String attributevalueid;
	private String attributevalue;
	private String isSubEntity;
	public String getAttributevalueid() {
		return attributevalueid;
	}
	public void setAttributevalueid(String attributevalueid) {
		this.attributevalueid = attributevalueid;
	}
	public String getAttributevalue() {
		return attributevalue;
	}
	public void setAttributevalue(String attributevalue) {
		this.attributevalue = attributevalue;
	}
	public String getIsSubEntity() {
		return isSubEntity;
	}
	public void setIsSubEntity(String isSubEntity) {
		this.isSubEntity = isSubEntity;
	}
	
	
}
